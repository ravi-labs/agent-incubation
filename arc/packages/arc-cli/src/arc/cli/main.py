@registry.command("submit")
@click.argument("manifest_path", default="manifest.yaml", required=False)
@click.option("--registry-dir", default=None, help="Path to local agent-registry checkout")
def registry_submit(manifest_path: str, registry_dir: str | None):
    """
    Prepare a manifest for submission to the central agent-registry.

    Validates the manifest, then either copies it to a local registry
    checkout (if --registry-dir is provided) or prints the PR checklist.

    Example:
        foundry registry submit
        foundry registry submit --registry-dir ../agent-registry
    """
    path = Path(manifest_path)
    manifest = _load_manifest_safe(path)

    # Run validation
    from arc.core.effects import effect_meta, DefaultDecision
    errors = []
    for effect in manifest.allowed_effects:
        meta = effect_meta(effect)
        if meta.default_decision == DefaultDecision.DENY:
            errors.append(f"Hard-denied effect: {effect.value}")

    if errors:
        click.echo(click.style("✗ Manifest cannot be submitted:", fg="red"))
        for e in errors:
            click.echo(f"  {e}")
        sys.exit(1)

    if registry_dir:
        import shutil
        dest = Path(registry_dir) / "registry" / manifest.agent_id
        dest.mkdir(parents=True, exist_ok=True)
        shutil.copy(path, dest / "manifest.yaml")
        click.echo(click.style(f"✓ Manifest copied to {dest}", fg="green"))
        click.echo("  Open a PR against the agent-registry repo to complete registration")
    else:
        click.echo(click.style("Registry submission checklist:", bold=True))
        click.echo()
        click.echo(f"  Agent:  {manifest.agent_id} v{manifest.version}")
        click.echo(f"  Owner:  {manifest.owner}")
        click.echo(f"  Stage:  {manifest.lifecycle_stage.value}")
        click.echo()
        click.echo("  PR steps:")
        click.echo("  1. Fork/clone: https://github.com/ravi-labs/agent-registry")
        click.echo(f"  2. Copy manifest: cp {path} registry/{manifest.agent_id}/manifest.yaml")
        click.echo(f"  3. git checkout -b register/{manifest.agent_id}")
        click.echo("  4. git add . && git commit -m 'Register {}'".format(manifest.agent_id))
        click.echo("  5. Open PR — compliance officer will review effects and policy")


