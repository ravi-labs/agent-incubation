@agent.command("new")
@click.argument("name")
@click.option("--dir", "output_dir", default=".", help="Directory to scaffold into (default: current dir)")
def agent_new(name: str, output_dir: str):
    """
    Scaffold a new agent directory.

    Creates NAME/manifest.yaml, NAME/policy.yaml, and NAME/agent.py
    pre-wired with the Foundry conventions and effect declarations.

    Example:
        foundry agent new fee-transparency
        foundry agent new market-risk-monitor --dir src/agents
    """
    # Sanitize name
    slug = name.lower().replace(" ", "-").replace("_", "-")
    class_name = "".join(w.title() for w in slug.split("-")) + "Agent"
    name_title = " ".join(w.title() for w in slug.split("-"))

    base = Path(output_dir) / slug
    if base.exists():
        click.echo(click.style(f"✗ Directory already exists: {base}", fg="red"), err=True)
        sys.exit(1)

    base.mkdir(parents=True)

    (base / "manifest.yaml").write_text(
        MANIFEST_TEMPLATE.format(name=slug)
    )
    (base / "policy.yaml").write_text(
        POLICY_TEMPLATE.format(name=slug)
    )
    (base / "agent.py").write_text(
        AGENT_TEMPLATE.format(
            name=slug,
            name_title=name_title,
            class_name=class_name,
        )
    )
    (base / "tests").mkdir()
    (base / "tests" / f"test_{slug.replace('-', '_')}.py").write_text(
        f'"""Tests for {name_title} agent."""\n\nimport pytest\n\n\n'
        f'# TODO: Add tests\n'
    )

    click.echo(click.style(f"✓ Scaffolded agent: {slug}/", fg="green"))
    click.echo(f"  {slug}/manifest.yaml  — declare effects and success metrics")
    click.echo(f"  {slug}/policy.yaml    — add any policy overrides")
    click.echo(f"  {slug}/agent.py       — implement your agent logic")
    click.echo(f"  {slug}/tests/         — write your tests")
    click.echo()
    click.echo(click.style("Next steps:", bold=True))
    click.echo(f"  1. Edit {slug}/manifest.yaml and set allowed_effects")
    click.echo(f"  2. Implement {class_name}.execute() in {slug}/agent.py")
    click.echo(f"  3. foundry agent validate {slug}/manifest.yaml")


