@agent.command("promote")
@click.argument("manifest_path", default="manifest.yaml", required=False)
@click.option("--to", "target_stage", required=False, help="Target stage (default: next stage)")
@click.option("--dry-run", is_flag=True, help="Show what would happen without writing")
def agent_promote(manifest_path: str, target_stage: str | None, dry_run: bool):
    """
    Promote an agent to the next lifecycle stage.

    Updates the lifecycle_stage field in the manifest.yaml and
    validates exit criteria for the current stage.

    Example:
        foundry agent promote
        foundry agent promote --to VALIDATE
        foundry agent promote --dry-run
    """
    from arc.core.lifecycle import LifecycleStage

    path = Path(manifest_path)
    manifest = _load_manifest_safe(path)

    stage_order = [s for s in LifecycleStage]
    current_idx = stage_order.index(manifest.lifecycle_stage)

    if target_stage:
        try:
            next_stage = LifecycleStage(target_stage.upper())
        except ValueError:
            valid = [s.value for s in LifecycleStage]
            click.echo(click.style(f"✗ Invalid stage '{target_stage}'. Must be one of: {valid}", fg="red"), err=True)
            sys.exit(1)
    else:
        if current_idx + 1 >= len(stage_order):
            click.echo(click.style("✗ Agent is already at the final stage (SCALE)", fg="yellow"))
            sys.exit(0)
        next_stage = stage_order[current_idx + 1]

    click.echo(f"Promoting: {manifest.agent_id}")
    click.echo(f"  {manifest.lifecycle_stage.value} → {next_stage.value}")

    if next_stage == LifecycleStage.SCALE:
        click.echo()
        click.echo(click.style("  ⚠ Promoting to SCALE sets environment to 'production'", fg="yellow"))

    if dry_run:
        click.echo()
        click.echo(click.style("Dry run — no changes written", fg="yellow"))
        return

    # Read raw YAML and update in-place
    with open(path) as f:
        content = f.read()

    content = content.replace(
        f"lifecycle_stage: {manifest.lifecycle_stage.value}",
        f"lifecycle_stage: {next_stage.value}",
    )

    if next_stage == LifecycleStage.SCALE:
        content = content.replace("environment: sandbox", "environment: production")

    with open(path, "w") as f:
        f.write(content)

    click.echo(click.style(f"✓ Promoted to {next_stage.value}", fg="green"))
    if next_stage == LifecycleStage.GOVERN:
        click.echo()
        click.echo("  Next step: open a PR to the agent-registry repo for compliance review")
        click.echo("  Run: foundry registry submit")


# ─── foundry agent suspend / resume ───────────────────────────────────────────

@agent.command("suspend")
@click.argument("manifest_path", default="manifest.yaml", required=False)
@click.option("--reason", required=True, help="Reason for suspension (required for audit trail)")
def agent_suspend(manifest_path: str, reason: str):
    """
    Suspend an agent (kill switch).

    Sets status: suspended in the manifest. The agent will be blocked
    from executing any effects until it is resumed.

    Example:
        foundry agent suspend --reason "Unexpected communication volume spike"
    """
    path = Path(manifest_path)
    manifest = _load_manifest_safe(path)

    from arc.core.manifest import AgentStatus
    if manifest.status == AgentStatus.SUSPENDED:
        click.echo(click.style(f"Agent '{manifest.agent_id}' is already suspended", fg="yellow"))
        return

    with open(path) as f:
        content = f.read()

    if f"status: {manifest.status.value}" in content:
        content = content.replace(f"status: {manifest.status.value}", "status: suspended")
    else:
        # Status not in file yet — add it
        content = content.replace(
            f"lifecycle_stage: {manifest.lifecycle_stage.value}",
            f"lifecycle_stage: {manifest.lifecycle_stage.value}\nstatus: suspended",
        )

    with open(path, "w") as f:
        f.write(content)

    click.echo(click.style(f"✓ Agent '{manifest.agent_id}' suspended", fg="red", bold=True))
    click.echo(f"  Reason: {reason}")
    click.echo(f"  To resume: foundry agent resume {manifest_path}")


@agent.command("resume")
@click.argument("manifest_path", default="manifest.yaml", required=False)
def agent_resume(manifest_path: str):
    """Resume a suspended agent."""
    path = Path(manifest_path)
    manifest = _load_manifest_safe(path)

    from arc.core.manifest import AgentStatus
    if manifest.status != AgentStatus.SUSPENDED:
        click.echo(click.style(f"Agent '{manifest.agent_id}' is not suspended (status: {manifest.status.value})", fg="yellow"))
        return

    with open(path) as f:
        content = f.read()

    content = content.replace("status: suspended", "status: active")
    with open(path, "w") as f:
        f.write(content)

    click.echo(click.style(f"✓ Agent '{manifest.agent_id}' resumed", fg="green"))


