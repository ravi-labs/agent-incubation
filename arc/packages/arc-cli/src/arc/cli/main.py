@agent.command("validate")
@click.argument("manifest_path", default="manifest.yaml", required=False)
@click.option("--strict", is_flag=True, help="Fail on warnings (use in CI)")
def agent_validate(manifest_path: str, strict: bool):
    """
    Validate a manifest.yaml file.

    Checks that all required fields are present, all declared effects
    exist in the taxonomy, and all policy references are valid.

    Example:
        foundry agent validate
        foundry agent validate examples/retirement_trajectory/manifest.yaml
        foundry agent validate --strict  # for CI gates
    """
    from arc.core.effects import FinancialEffect, effect_meta
    from arc.core.manifest import AgentStatus

    path = Path(manifest_path)
    manifest = _load_manifest_safe(path)

    warnings = []
    errors = []

    # Check: status
    if manifest.status == AgentStatus.SUSPENDED:
        warnings.append("Agent status is 'suspended' — agent will not execute until resumed")
    if manifest.status == AgentStatus.DEPRECATED:
        warnings.append("Agent status is 'deprecated'")

    # Check: lifecycle stage vs environment
    from arc.core.lifecycle import LifecycleStage
    if manifest.lifecycle_stage == LifecycleStage.SCALE and manifest.environment != "production":
        warnings.append("Lifecycle stage is SCALE but environment is not 'production'")
    if manifest.lifecycle_stage in (LifecycleStage.DISCOVER, LifecycleStage.SHAPE) and manifest.environment == "production":
        errors.append("Production environment is not allowed before stage VALIDATE")

    # Check: effects exist in taxonomy
    for effect in manifest.allowed_effects:
        meta = effect_meta(effect)
        from arc.core.effects import DefaultDecision
        if meta.default_decision == DefaultDecision.DENY:
            errors.append(
                f"Effect '{effect.value}' has a hard DENY default — "
                f"it cannot be declared in allowed_effects"
            )

    # Check: policy path exists
    policy_path = Path(manifest.policy_path)
    if not policy_path.exists():
        # Also try relative to manifest directory
        alt_path = path.parent / "policy.yaml"
        if not alt_path.exists():
            warnings.append(f"Policy file not found: {manifest.policy_path}")

    # Check: success metrics
    if not manifest.success_metrics:
        errors.append("success_metrics must have at least one entry")
    for m in manifest.success_metrics:
        if m.startswith("TODO"):
            warnings.append(f"Success metric appears to be a placeholder: '{m}'")

    # Check: team_repo
    if not manifest.team_repo:
        warnings.append("team_repo is not set — recommended for registry submissions")

    # Print results
    click.echo(click.style(f"Validating: {path}", bold=True))
    click.echo(f"  Agent:    {manifest.agent_id} v{manifest.version}")
    click.echo(f"  Stage:    {manifest.lifecycle_stage.value}")
    click.echo(f"  Status:   {manifest.status.value}")
    click.echo(f"  Effects:  {len(manifest.allowed_effects)} declared")
    click.echo()

    for w in warnings:
        click.echo(click.style(f"  ⚠ {w}", fg="yellow"))

    for e in errors:
        click.echo(click.style(f"  ✗ {e}", fg="red"))

    if errors:
        click.echo()
        click.echo(click.style(f"✗ Validation failed — {len(errors)} error(s)", fg="red"))
        sys.exit(1)
    elif warnings and strict:
        click.echo()
        click.echo(click.style(f"✗ Strict mode: {len(warnings)} warning(s)", fg="red"))
        sys.exit(1)
    else:
        click.echo()
        click.echo(click.style("✓ Manifest is valid", fg="green"))
        if warnings:
            click.echo(f"  ({len(warnings)} warning(s) — run without --strict to allow)")


