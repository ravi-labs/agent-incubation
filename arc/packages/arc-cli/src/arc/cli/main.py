def _load_manifest_safe(path: Path):
    """Load manifest, printing a helpful error and exiting on failure."""
    from arc.core.manifest import load_manifest
    try:
        return load_manifest(path)
    except FileNotFoundError:
        click.echo(click.style(f"✗ No manifest found at: {path}", fg="red"), err=True)
        sys.exit(1)
    except ValueError as e:
        click.echo(click.style(f"✗ Invalid manifest: {e}", fg="red"), err=True)
        sys.exit(1)


