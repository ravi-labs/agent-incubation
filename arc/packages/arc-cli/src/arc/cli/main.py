@agent.command("list")
@click.option("--dir", "search_dir", default=".", help="Root directory to search (default: current dir)")
@click.option("--format", "fmt", type=click.Choice(["table", "json"]), default="table")
def agent_list(search_dir: str, fmt: str):
    """
    List all agents found in a directory tree.

    Finds every manifest.yaml under the given directory and prints
    a summary table. Useful for browsing agents in a team repo or registry.

    Example:
        foundry agent list
        foundry agent list --dir examples
        foundry agent list --format json
    """
    from arc.core.manifest import load_manifest

    manifests = list(Path(search_dir).rglob("manifest.yaml"))
    if not manifests:
        click.echo("No agents found.")
        return

    agents = []
    for p in sorted(manifests):
        try:
            m = load_manifest(p)
            agents.append(m)
        except Exception:
            pass  # Skip invalid manifests silently in list

    if fmt == "json":
        click.echo(json.dumps([a.to_dict() for a in agents], indent=2))
        return

    # Table output
    col_w = [28, 10, 12, 11, 8]
    header = (
        f"{'AGENT':<{col_w[0]}} {'VERSION':<{col_w[1]}} "
        f"{'STAGE':<{col_w[2]}} {'STATUS':<{col_w[3]}} {'ENV':<{col_w[4]}}"
    )
    click.echo(click.style(header, bold=True))
    click.echo("─" * sum(col_w + [4]))

    for m in agents:
        status_color = {"active": "green", "suspended": "red", "deprecated": "yellow"}.get(
            m.status.value, "white"
        )
        row = (
            f"{m.agent_id:<{col_w[0]}} {m.version:<{col_w[1]}} "
            f"{m.lifecycle_stage.value:<{col_w[2]}} "
            f"{click.style(m.status.value, fg=status_color):<{col_w[3]+9}} "
            f"{m.environment:<{col_w[4]}}"
        )
        click.echo(row)

    click.echo()
    click.echo(f"{len(agents)} agent(s) found")


